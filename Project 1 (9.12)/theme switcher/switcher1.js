function darkModeClick()
{
    document.querySelector("body").style.backgroundColor="red";
    document.querySelector("button").style.color="blue";
    document.querySelector("button").style.border="violet";
}
 function lightModeClick()
 {
    document.querySelector("body").style.backgroundColor="yellow";
    document.querySelector("button").style.color="pink";
    document.querySelector("button").style.border="green";
 }
    





