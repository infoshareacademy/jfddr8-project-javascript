function darkModeClick()
{
    document.querySelector("body").style.backgroundColor="white";
    document.querySelector("btn btnDark").style.color="black";
    document.querySelector("btn btnDark").style.border="white";
}
 function lightModeClick()
 {
    document.querySelector("body").style.backgroundColor="white";
    document.querySelector("btn btnLight").style.color="black";
    document.querySelector("btn btnLight").style.border="white";

 }


 




